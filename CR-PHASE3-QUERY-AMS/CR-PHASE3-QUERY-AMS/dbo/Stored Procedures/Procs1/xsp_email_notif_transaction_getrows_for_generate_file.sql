CREATE procedure dbo.xsp_email_notif_transaction_getrows_for_generate_file
as
begin
	declare @msg nvarchar(max) ;

	begin try
		select distinct
				mail_file_name
				,mail_file_path
		from	dbo.email_notif_transaction
		where	mail_status				 = 'PENDING'
				and generate_file_status = 'PENDING' ;
	end try
	begin catch
		declare @error int ;

		set @error = @@error ;

		if (@error = 2627)
		begin
			set @msg = dbo.xfn_get_msg_err_code_already_exist() ;
		end ;

		if (len(@msg) <> 0)
		begin
			set @msg = 'V' + ';' + @msg ;
		end ;
		else
		begin
			if (
				   error_message() like '%V;%'
				   or	error_message() like '%E;%'
			   )
			begin
				set @msg = error_message() ;
			end ;
			else
			begin
				set @msg = 'E;' + dbo.xfn_get_msg_err_generic() + ';' + error_message() ;
			end ;
		end ;

		raiserror(@msg, 16, -1) ;

		return ;
	end catch ;
end ;
