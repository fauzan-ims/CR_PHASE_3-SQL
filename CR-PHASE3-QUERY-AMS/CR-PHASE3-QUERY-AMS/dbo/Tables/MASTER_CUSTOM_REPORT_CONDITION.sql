			          INSERT INTO [dbo].[Z_AUDIT_MASTER_CUSTOM_REPORT_CONDITION]
([ID],[CUSTOM_REPORT_CODE],[LOGICAL_OPERATOR],[COLUMN_NAME],[COMPARISON_OPERATOR],[START_VALUE],[END_VALUE],[ORDER_KEY],[CRE_DATE],[CRE_BY],[CRE_IP_ADDRESS],[MOD_DATE],[MOD_BY],[MOD_IP_ADDRESS],AuditDataState,AuditDMLAction,AuditUser,AuditDateTime,UpdateColumns)
