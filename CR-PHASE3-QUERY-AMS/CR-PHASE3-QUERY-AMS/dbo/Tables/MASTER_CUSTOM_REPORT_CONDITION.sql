CREATE TABLE [dbo].[MASTER_CUSTOM_REPORT_CONDITION] (
    [ID]                  BIGINT          IDENTITY (1, 1) NOT NULL,
    [CUSTOM_REPORT_CODE]  NVARCHAR (50)   CONSTRAINT [DF_MASTER_CUSTOM_REPORT_CONDITION_CUSTOM_REPORT_CODE] DEFAULT ('') NULL,
    [LOGICAL_OPERATOR]    NVARCHAR (20)   CONSTRAINT [DF_MASTER_CUSTOM_REPORT_CONDITION_LOGICAL_OPERATOR] DEFAULT ('') NULL,
    [COLUMN_NAME]         NVARCHAR (250)  CONSTRAINT [DF_MASTER_CUSTOM_REPORT_CONDITION_COLUMN_NAME] DEFAULT ('') NULL,
    [COMPARISON_OPERATOR] NVARCHAR (20)   CONSTRAINT [DF_MASTER_CUSTOM_REPORT_CONDITION_COMPARISON_OPERATOR] DEFAULT ('') NULL,
    [START_VALUE]         NVARCHAR (4000) CONSTRAINT [DF_MASTER_CUSTOM_REPORT_CONDITION_START_VALUE] DEFAULT ('') NULL,
    [END_VALUE]           NVARCHAR (4000) CONSTRAINT [DF_MASTER_CUSTOM_REPORT_CONDITION_END_VALUE] DEFAULT ('') NULL,
