CREATE PROCEDURE dbo.xsp_opl_interface_deposit_revenue_detail_insert
(
	@p_deposit_revenue_code nvarchar(50)
	,@p_deposit_code		nvarchar(50)
	,@p_deposit_type		nvarchar(15)
	,@p_deposit_amount		decimal(18, 2)
	,@p_revenue_amount		decimal(18, 2)
	--															
	,@p_cre_date			datetime
	,@p_cre_by				nvarchar(15)
	,@p_cre_ip_address		nvarchar(15)
	,@p_mod_date			datetime
	,@p_mod_by				nvarchar(15)
	,@p_mod_ip_address		nvarchar(15)
)
as
begin
	declare @msg nvarchar(max) ;

	begin try
		insert into dbo.opl_interface_deposit_revenue_detail
		(
			deposit_revenue_code
			,deposit_code
			,deposit_type
			,deposit_amount
			,revenue_amount
			--
			,cre_date
			,cre_by
			,cre_ip_address
			,mod_date
			,mod_by
			,mod_ip_address
		)
		values
		(	@p_deposit_revenue_code
			,@p_deposit_code
			,@p_deposit_type
			,@p_deposit_amount
			,@p_revenue_amount
			--						
			,@p_cre_date
			,@p_cre_by
			,@p_cre_ip_address
			,@p_mod_date
			,@p_mod_by
			,@p_mod_ip_address
		) ; 
	end try
	begin catch
		declare @error int ;

		set @error = @@error ;

		if (@error = 2627)
		begin
			set @msg = dbo.xfn_get_msg_err_code_already_exist() ;
		end ;

		if (len(@msg) <> 0)
		begin
			set @msg = 'V' + ';' + @msg ;
		end ;
		else
		begin
			if (
				   error_message() like '%V;%'
				   or	error_message() like '%E;%'
			   )
			begin
				set @msg = error_message() ;
			end ;
			else
			begin
				set @msg = 'E;' + dbo.xfn_get_msg_err_generic() + ';' + error_message() ;
			end ;
		end ;

		raiserror(@msg, 16, -1) ;

		return ;
	end catch ;
end ;
