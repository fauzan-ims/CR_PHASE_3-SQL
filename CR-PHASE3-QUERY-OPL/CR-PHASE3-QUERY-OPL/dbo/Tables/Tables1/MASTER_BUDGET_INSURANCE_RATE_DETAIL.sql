CREATE TABLE [dbo].[MASTER_BUDGET_INSURANCE_RATE_DETAIL] (
    [ID]                         BIGINT          IDENTITY (1, 1) NOT NULL,
    [BUDGET_INSURANCE_RATE_CODE] NVARCHAR (50)   NOT NULL,
    [SUM_INSURED_FROM]           DECIMAL (18, 2) NOT NULL,
    [SUM_INSURED_TO]             DECIMAL (18, 2) NOT NULL,
    [REGION_CODE]                NVARCHAR (50)   NOT NULL,
    [REGION_DESCRIPTION]         NVARCHAR (250)  NOT NULL,
    [RATE_1]                     DECIMAL (9, 6)  NOT NULL,
    [RATE_2]                     DECIMAL (9, 6)  NOT NULL,
    [RATE_3]                     DECIMAL (9, 6)  NOT NULL,
    [RATE_4]                     DECIMAL (9, 6)  NOT NULL,
    [CRE_DATE]                   DATETIME        NOT NULL,
    [CRE_BY]                     NVARCHAR (15)   NOT NULL,
    [CRE_IP_ADDRESS]             NVARCHAR (15)   NOT NULL,
    [MOD_DATE]                   DATETIME        NOT NULL,
    [MOD_BY]                     NVARCHAR (15)   NOT NULL,
    [MOD_IP_ADDRESS]             NVARCHAR (15)   NOT NULL,
    CONSTRAINT [PK_MASTER_BUDGET_ISURANCE_RATE_DETAIL] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [FK_MASTER_BUDGET_INSURANCE_RATE_DETAIL_MASTER_BUDGET_INSURANCE_RATE] FOREIGN KEY ([BUDGET_INSURANCE_RATE_CODE]) REFERENCES [dbo].[MASTER_BUDGET_INSURANCE_RATE] ([CODE]) ON DELETE CASCADE ON UPDATE CASCADE
);


GO
    
