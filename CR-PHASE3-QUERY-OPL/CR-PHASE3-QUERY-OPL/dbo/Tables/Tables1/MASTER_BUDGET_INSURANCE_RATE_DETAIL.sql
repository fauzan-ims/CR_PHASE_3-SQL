([ID],[BUDGET_INSURANCE_RATE_CODE],[SUM_INSURED_FROM],[SUM_INSURED_TO],[REGION_CODE],[REGION_DESCRIPTION],[RATE_1],[RATE_2],[RATE_3],[RATE_4],[CRE_DATE],[CRE_BY],[CRE_IP_ADDRESS],[MOD_DATE],[MOD_BY],[MOD_IP_ADDRESS],AuditDataState,AuditDMLAction,AuditUser,AuditDateTime,UpdateColumns)
