CREATE TABLE [dbo].[APPLICATION_DOC] (
    [ID]                 BIGINT          IDENTITY (1, 1) NOT NULL,
    [APPLICATION_NO]     NVARCHAR (50)   NOT NULL,
    [DOCUMENT_CODE]      NVARCHAR (50)   NOT NULL,
    [FILENAME]           NVARCHAR (250)  NOT NULL,
    [PATHS]              NVARCHAR (250)  NOT NULL,
    [EXPIRED_DATE]       DATETIME        NULL,
    [PROMISE_DATE]       DATETIME        NULL,
    [IS_REQUIRED]        NVARCHAR (1)    NOT NULL,
    [WAIVE_STATUS]       NVARCHAR (10)   NULL,
    [WAIVE_REQUEST_DATE] DATETIME        NULL,
    [WAIVE_APPROVE_BY]   NVARCHAR (50)   NULL,
    [WAIVE_APPROVE_DATE] DATETIME        NULL,
    [IS_VALID]           NVARCHAR (1)    CONSTRAINT [DF_APPLICATION_DOC_IS_VALID] DEFAULT ((0)) NULL,
    [CRE_DATE]           DATETIME        NOT NULL,
    [CRE_BY]             NVARCHAR (15)   NOT NULL,
    [CRE_IP_ADDRESS]     NVARCHAR (15)   NOT NULL,
    [MOD_DATE]           DATETIME        NOT NULL,
    [MOD_BY]             NVARCHAR (15)   NOT NULL,
    [MOD_IP_ADDRESS]     NVARCHAR (15)   NOT NULL,
    [REMARKS]            NVARCHAR (4000) NULL,
    [RECEIVED_DATE]      DATETIME        NULL,
    [IS_RECEIVED]        NVARCHAR (1)    NULL,
    [IS_EDIT]            NVARCHAR (1)    NULL,
    CONSTRAINT [PK_APPLICATION_DOC] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [FK_APPLICATION_DOC_APPLICATION_MAIN] FOREIGN KEY ([APPLICATION_NO]) REFERENCES [dbo].[APPLICATION_MAIN] ([APPLICATION_NO]) ON DELETE CASCADE ON UPDATE CASCADE,
