CREATE TABLE [dbo].[MASTER_FINANCIAL_STATEMENT] (
    [CODE]           NVARCHAR (50)  NOT NULL,
    [REPORT_TYPE]    NVARCHAR (50)  NOT NULL,
    [DESCRIPTION]    NVARCHAR (250) NOT NULL,
    [PARENT_CODE]    NVARCHAR (50)  NULL,
    [TYPE]           NVARCHAR (10)  NOT NULL,
    [LEVEL_KEY]      INT            NOT NULL,
    [ORDER_KEY]      INT            NOT NULL,
    [IS_ACTIVE]      NVARCHAR (1)   NOT NULL,
    [CRE_DATE]       DATETIME       NOT NULL,
    [CRE_BY]         NVARCHAR (15)  NOT NULL,
    [CRE_IP_ADDRESS] NVARCHAR (15)  NOT NULL,
    [MOD_DATE]       DATETIME       NOT NULL,
    [MOD_BY]         NVARCHAR (15)  NOT NULL,
    [MOD_IP_ADDRESS] NVARCHAR (15)  NOT NULL,
    CONSTRAINT [PK_MASTER_FINANCIAL_STATEMENT] PRIMARY KEY CLUSTERED ([CODE] ASC, [REPORT_TYPE] ASC),
    CONSTRAINT [FK_MASTER_FINANCIAL_STATEMENT_SYS_GENERAL_SUBCODE] FOREIGN KEY ([REPORT_TYPE]) REFERENCES [dbo].[SYS_GENERAL_SUBCODE] ([CODE])
);


GO
    
